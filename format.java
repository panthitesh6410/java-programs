public class format
{  
    public static void main(String args[])
    {
        System.out.println("my name is: \n HITESH PANT");    //use of "\n"
        System.out.println("my name is: \t HITESH PANT");    //use of "\t"
        System.out.println("my name is \r HITESH PANT");     //use of "\r"
        System.out.println("my name is: \b HITESH PANT");    //use of "\b"
        System.out.println("what my name is? \n\f HITESH PANT \n\f AMAN SINGH \n\f RANI BAI");  //use of "\f"
    }
    
}
