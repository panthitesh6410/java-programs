//DIFFERENT TYPES OF DATATYPES:

public class dataypes 
{
    public static void main(String args[])
    {
        byte rno=50;
        System.out.println("byte:"+rno);
        short height=2000;
        System.out.println("short:"+height);
        int dist=10000;
        System.out.println("int:"+dist);
        long distance=50000000;
        System.out.println("long:"+distance);
        
        float pi=3.142f;
        System.out.println("\nfloat:"+pi);
        double age=1.234e6;
        System.out.println("double:"+age);
        
        char ch='h';
        System.out.println("\nchar:"+ch);
        char symbol='@';
        System.out.println("char:"+symbol);
        
        String str="my name is hitesh pant";
        System.out.println("\nstring:"+str);
        
        boolean answer1=true;
        boolean answer2=false;
        System.out.println("\nboolean:"+answer1);
        System.out.println("boolean:"+answer2);
        
        }
    
}
