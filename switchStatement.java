public class switchStatement 
{
    public static void main(String args[])
    {
        char color='g';
        switch(color)
        {
            case 'r':   System.out.println("red");
            case 'g':   System.out.println("green");
            case 'b':   System.out.println("blue");
            default:    System.out.println("no color");
               
        }
        
    }
    
}
